import { Expose, Transform } from 'class-transformer';

export class ReportDto {
  @Expose()
  id: number;

  @Expose()
  price: number;

  @Expose()
  make: string;

  @Expose()
  approved: boolean;

  @Expose()
  model: string;

  @Expose()
  year: number;

  @Expose()
  lng: number;

  @Expose()
  lat: number;

  @Expose()
  mileage: number;

  //remueve objeto completo de user y lo transforma a solo el id
  @Transform(({ obj }) => obj.user.id)
  @Expose()
  userId: number;
}
